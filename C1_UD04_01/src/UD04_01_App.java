
public class UD04_01_App {

	public static void main(String[] args) {
		
//		1- Declara dos variables numéricas(con el valor que desees),muestra por consola la suma,
//		resta,multiplicación,división y módulo(resto de la división).
		
//		Variables
		int num1 = 10;
		int num2 = 5;
		
//		Programa
		System.out.println("Suma " + num1 + " + " + num2 + " = " + (num1+num2));
		System.out.println("Resta " + num1 + " - " + num2 + " = " + (num1-num2));
		System.out.println("Multiplicacion " + num1 + " * " + num2 + " = " + (num1*num2));
		System.out.println("Division " + num1 + " / " + num2 + " = " + (num1/num2));
		System.out.println("Modulo " + num1 + " % " + num2 + " = " + (num1%num2));

	}

}
